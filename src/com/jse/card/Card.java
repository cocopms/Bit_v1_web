package com.jse.card;

public class Card {
	private String kind;
	private int number;

	public void setKind(String kind) {
		this.kind = kind;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public String getKind() {
		return kind;
	}

	public int getNumber() {
		return number;
	}
}